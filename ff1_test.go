func testFF1(t *testing.T, K, T []byte, PT, CT string, r int) {
	if len(PT) != len(CT) {
		t.FailNow()
	}

	ff1, err := NewFF1(K, T, 0, 0, r)
	if err != nil {
		t.Fatal(err)
	}

	out, err := ff1.Encrypt(PT, nil)
	if err != nil {
		t.Fatal(err)
	}

	if out != CT {
		t.FailNow()
	}

	out, err = ff1.Decrypt(CT, nil)
	if err != nil {
		t.Fatal(err)
	}

	if out != PT {
		t.FailNow()
	}
}

